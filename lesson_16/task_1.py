class Cash:
#Инициализирует кассу с начальным количеством денег
    def __init__(self, initial_amount=0):
        self.balance = initial_amount

#Пополняет кассу на X единиц
    def top_up(self, X):
        if X > 0:
            self.balance += X
        else:
            print("Ошибка: сумма пополнения должна быть положительной.")

#Выводит количество целых тысяч в текущем балансе кассы
    def count_1000(self):
        thousands = self.balance // 1000
        print(f"Целых тысяч в кассе: {thousands}")

#Забирает X единиц из кассы, если достаточно средств 
    def take_away(self, X):
        if X <= 0:
            print("Ошибка: сумма изъятия должна быть положительной.")
            return

        if self.balance >= X:
            self.balance -= X
            print(f"Из кассы изъято {X}. Текущий баланс: {self.balance}.")
        else:
            print(f"Ошибка: недостаточно средств в кассе. Текущий баланс: {self.balance}, запрашиваемая сумма: {X}.")

# Пример использования:
if __name__ == "__main__":
    # Создаем кассу с начальным балансом 5000
    cash_box = Cash(5000)

    # Пополняем кассу на 2500
    cash_box.top_up(2500)
    print(f"Текущий баланс: {cash_box.balance}")  # 7500

    # Считаем целые тысячи
    cash_box.count_1000()  # Выведет: Целых тысяч в кассе: 7

    # Забираем 3000 из кассы
    cash_box.take_away(3000)  # Из кассы изъято 3000. Текущий баланс: 4500.

    # Пытаемся забрать больше, чем есть в кассе
    cash_box.take_away(5000)  # Ошибка: недостаточно средств...

    # Проверяем баланс
    print(f"Финальный баланс: {cash_box.balance}")  # 4500
