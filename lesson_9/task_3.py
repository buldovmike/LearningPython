# Считываем строку чисел и разбиваем на отдельные элементы
numbers = input().split()

# Создаём пустое множество для хранения уже встреченных чисел
seen = set()

# Обрабатываем каждое число по очереди
for num_str in numbers:
    # Преобразуем строку в число (для корректного сравнения)
    num = int(num_str)
    
    # Проверяем, было ли число уже встречено
    if num in seen:
        print("YES")
    else:
        print("NO")
        # Добавляем число во множество, так как теперь оно встретилось
        seen.add(num)