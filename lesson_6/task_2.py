x = int(input("Введите натурально число "))
c = 0
d = 1

# Перебираем делители до квадратного корня из x
while d * d <= x:
    if x % d == 0:
        if d * d == x:
            c += 1  # d — точный квадратный корень, добавляем 1
        else:
            c += 2  # добавляем пару делителей: d и x // d
    d += 1
print(c)