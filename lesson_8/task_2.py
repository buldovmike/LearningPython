n = int(input("Введите число"))

# Проверка: 1 ≤ n ≤ 100 000
if not (1 <= n <= 100000):
    print("Ошибка: число должно быть от 1 до 100 000")
else:
    arr = list(map(int, input().split()))
    
    # Проверка количества чисел
    if len(arr) != n:
        print("Ошибка: введено неверное количество чисел")
    else:
        # Проверка каждого числа: 1 ≤ Ai ≤ 10e9
        valid = True
        for num in arr:
            if not (1 <= num <= 10**9):
                valid = False
                break 
        if not valid:
            print("Ошибка: все числа должны быть от 1 до 1 000 000 000")
        else:
            # Преобразуем массив: последний элемент — на первое место, остальные сдвигаем вправо
            result = [arr[-1]] + arr[:-1]
            # Выводим результат через пробел
            print(*result)